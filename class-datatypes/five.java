public class five {
    public static void main(String[] args) {
        
        byte A = 100;          
        short b = 10000;      
        int c = 100000;          
        long d = 10000000000L;  

    
        float e = 10.5f;       
             
        
        System.out.println("byte: " + A);
        System.out.println("short: " + b);
        System.out.println("int: " + c);
        System.out.println("long: " + d);
        System.out.println("float: " + e);
        
    }
}
