public class pdtobj {
    public static void main(String[] args) {

        
        int i = 10;
        float f = 12.5f;
        char c = 'A';
        boolean b = true;
        Integer intObj = i;
        Float floatObj = f;
        Character charObj = c;
        Boolean boolObj = b;

        // Print wrapper objects
        System.out.println(intObj);
        System.out.println(floatObj);
        System.out.println(charObj);
        System.out.println(boolObj);
    }
}
