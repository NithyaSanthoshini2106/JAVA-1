public class whileone {

    public static void main(String[] args) {

        int[] numbers = {10, 20, 30, 40, 50};

        

        // 3️⃣ While Loop
        System.out.println("\nUsing while loop:");
        int i = 0;
        while (i < numbers.length) {
            System.out.println(numbers[i]);
            i++;
        }

        

    }
}
